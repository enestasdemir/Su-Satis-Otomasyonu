package uygulama;

public class Siparis {

    private String ID, madi, msoyadi, madres, durum, tutar, tarih;

    public String getTarih() {
        return tarih;
    }

    public void setTarih(String tarih) {
        this.tarih = tarih;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getDurum() {
        return durum;
    }

    public void setDurum(String durum) {
        this.durum = durum;
    }

    public String getTutar() {
        return tutar;
    }

    public void setTutar(String tutar) {
        this.tutar = tutar;
    }

    public String getMAdi() {
        return madi;
    }

    public void setMAdi(String madi) {
        this.madi = madi;
    }

    public String getMSoyadi() {
        return msoyadi;
    }

    public void setMSoyadi(String msoyadi) {
        this.msoyadi = msoyadi;
    }

    public String getMAdres() {
        return madres;
    }

    public void setMAdres(String madres) {
        this.madres = madres;
    }
}
